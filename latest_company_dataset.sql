-- 查看資料匯入結果
SELECT '樓層資訊' as '資料表', COUNT(*) as '筆數' FROM floor_info
UNION ALL
SELECT '部門聯絡', COUNT(*) FROM department_contacts  
UNION ALL
SELECT '員工座位', COUNT(*) FROM employee_seats
UNION ALL
SELECT '分機號碼', COUNT(*) FROM extension_numbers;

-- 查看各樓層人員分布
SELECT 
    f.floor_number as '樓層',
    f.floor_name as '樓層名稱',
    COUNT(es.id) as '員工數量'
FROM floor_info f
LEFT JOIN employee_seats es ON f.floor_number = es.floor_number
GROUP BY f.floor_number, f.floor_name
ORDER BY f.floor_number DESC;

